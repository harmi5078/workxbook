package com.book.echart.bean;

public class Chainlink {

	private EchartLine line;
	private EchartNode source;
	private EchartNode target;

	public EchartLine getLine() {
		return line;
	}

	public void setLine(EchartLine line) {
		this.line = line;
	}

	public EchartNode getSource() {
		return source;
	}

	public void setSource(EchartNode source) {
		this.source = source;
	}

	public EchartNode getTarget() {
		return target;
	}

	public void setTarget(EchartNode target) {
		this.target = target;
	}

}
